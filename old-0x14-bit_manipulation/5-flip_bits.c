/**
* flip_bits - func name
* @n: func arg 1
* @m: func arg 2
* Return: unsigned int
*/
unsigned int flip_bits(unsigned long int n, unsigned long int m)
{
	unsigned long int d;
	unsigned int count = 0;

	d = n ^ m;
	while (d > 0)
	{
	if (d & 1)
		count++;
	d >>= 1;
	}
	return (count);
}

