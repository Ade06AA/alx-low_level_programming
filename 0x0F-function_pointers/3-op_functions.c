/**
* op_add - func name
 * add two int
* @a: func arg 1- type int
 * first int to be added
* @b: func arg 2 - type int
 * second int to be added
* Return: value of the added ints
*/
int op_add(int a, int b)
{
	return (a + b);
}
/**
* op_sub - func name
 * sub two int
* @a: func arg 1- type int
 * first int to be sub
* @b: func arg 2 - type int
 * second int to be sub
* Return: value of the subtracted ints
*/
int op_sub(int a, int b)
{
	return (a - b);
}
/**
* op_mul - func name
 * mul two int
* @a: func arg 1- type int
 * first int to be mul
* @b: func arg 2 - type int
 * second int to be mul
* Return: value of the multiplied ints
*/
int op_mul(int a, int b)
{
	return (a * b);
}
/**
* op_div - func name
 * div two int
* @a: func arg 1- type int
 * first int to be div
* @b: func arg 2 - type int
 * second int to be div
* Return: value of the divided ints
*/
int op_div(int a, int b)
{
	return (a / b);
}
/**
* op_mod - func name
 * mod two int
* @a: func arg 1- type int
 * first int to be mod
* @b: func arg 2 - type int
 * second int to be mod
* Return: remainder of the divisin of the two ints
*/

int op_mod(int a, int b)
{
	return (a % b);
}
